#Laço de repetição

resposta_certa = 10+5
resporta_usuario= int(input('Quanto é 10+5? '))
while resposta_certa != resporta_usuario:
    print('Você errou, tente novamente: ')
    resporta_usuario= int(input('Quanto é 10+5? '))
    if resposta_certa == resporta_usuario:
        print('Você acertou')
    